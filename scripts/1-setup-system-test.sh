PGID=${PGID}

# ── PLATFORM SETTINGS ───────────────────────────────────────────────────────
PLATFORM_ARCH=${PLATFORM_ARCH}
GPU_TYPE=${GPU_TYPE}

# ── PATHS (DERIVED) ───────────────────────────────────────────────────────
BASE_DIR=${BASE_DIR}
