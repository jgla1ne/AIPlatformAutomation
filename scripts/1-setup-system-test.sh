REDIS_ENABLED=true
OLLAMA_ENABLED=true
LITELLM_ENABLED=true
OPEN_WEBUI_ENABLED=true
QDRANT_ENABLED=true
CADDY_ENABLED=false

# Ports
POSTGRES_PORT=5432
REDIS_PORT=6379
OLLAMA_PORT=11434
LITELLM_PORT=4000
OPEN_WEBUI_PORT=3000
QDRANT_PORT=6333

# Database
POSTGRES_USER=aiplatform
POSTGRES_PASSWORD=aiplatform123
POSTGRES_DB=aiplatform

# Paths
BASE_DIR="/mnt/${TENANT_ID}"
CONFIG_DIR="${BASE_DIR}/config"
DATA_DIR="${BASE_DIR}/data"
LOGS_DIR="${BASE_DIR}/logs"
DOCKER_NETWORK="${PREFIX}-${TENANT_ID}-network"
