}

add_litellm() {
    cat >> "${COMPOSE_FILE}" << EOF

  litellm:
    image: ghcr.io/berriai/litellm:main-latest
    restart: unless-stopped
    networks:
      - default
    ports:
      - "\${LITELLM_PORT:-4000}:4000"
    environment:
      LITELLM_PORT: 4000
    volumes:
      - \${TENANT_DIR}/litellm:/app/data
