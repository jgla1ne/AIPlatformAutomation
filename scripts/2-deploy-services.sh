#!/bin/bash
# Script 2: Deploy Services - Final Corrected Version

set -euo pipefail

# --- Colors ---
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
NC='\033[0m'

# --- Early Logging ---
log()  { echo -e "${GREEN}[$(date +'%H:%M:%S')]${NC} $1"; }
warn() { echo -e "${YELLOW}[$(date +'%H:%M:%S')]${NC} $1"; }
error(){ echo -e "${RED}[$(date +'%H:%M:%S')]${NC} $1"; exit 1; }

log "Starting Script 2: Service Deployment"

# --- Environment Detection ---
# Find the most recently modified tenant directory in /mnt/data
TENANT_DIR=$(find /mnt/data -mindepth 1 -maxdepth 1 -type d -printf '%T@ %p\n' | sort -nr | head -n 1 | cut -d' ' -f2-)

if [[ -z "${TENANT_DIR}" || ! -d "${TENANT_DIR}" ]]; then
    error "Could not find a tenant directory in /mnt/data. Please run script 1 first."
fi

log "Detected tenant directory: ${TENANT_DIR}"

# Look for the .env file ONLY within that tenant directory
ENV_FILE="${TENANT_DIR}/.env"
if [[ ! -f "${ENV_FILE}" ]]; then
    error "Could not find a .env file at ${ENV_FILE}. Run script 1 first."
fi

log "Loading environment from: ${ENV_FILE}"
source "${ENV_FILE}"

# --- Structured Logging Setup ---
# This will now work because DATA_ROOT is guaranteed to be loaded
LOG_DIR="${DATA_ROOT}/logs"
mkdir -p "${LOG_DIR}"
chown "${TENANT_UID}:${TENANT_GID}" "${LOG_DIR}"
LOG_FILE="${LOG_DIR}/script-2-$(date +%Y%m%d-%H%M%S).log"

# Redirect all subsequent output to the log file and the screen
exec > >(tee -a "${LOG_FILE}") 2>&1

log "======= AI Platform Deployment Starting ======="
log "All subsequent output will be logged to: ${LOG_FILE}"

# --- Key Variables ---
COMPOSE_FILE="${DATA_ROOT}/docker-compose.yml"

# --- Docker Check ---
if ! docker info &>/dev/null; then
    error "Docker is not running. Please start Docker and try again."
fi
log "Docker is active."

# --- Build docker-compose.yml ---
log "Generating docker-compose.yml..."

# Start with header. NO version attribute.
cat > "${COMPOSE_FILE}" << EOF
# Auto-generated by deployment script
services:
EOF

# --- Service Definitions ---
if [[ "${ENABLE_POSTGRES}" == "true" ]]; then
cat >> "${COMPOSE_FILE}" << EOF
  postgres:
    image: postgres:15-alpine
    restart: unless-stopped
    networks: ["${DOCKER_NETWORK}"]
    environment:
      POSTGRES_USER: ${POSTGRES_USER}
      POSTGRES_PASSWORD: ${POSTGRES_PASSWORD}
      POSTGRES_DB: ${POSTGRES_DB}
    volumes:
      - ${DATA_ROOT}/postgres:/var/lib/postgresql/data
EOF
fi

if [[ "${ENABLE_REDIS}" == "true" ]]; then
cat >> "${COMPOSE_FILE}" << EOF
  redis:
    image: redis:7-alpine
    restart: unless-stopped
    networks: ["${DOCKER_NETWORK}"]
    command: redis-server --requirepass ${REDIS_PASSWORD}
    volumes:
      - ${DATA_ROOT}/redis:/data
EOF
fi

if [[ "${ENABLE_CADDY}" == "true" ]]; then
cat >> "${COMPOSE_FILE}" << EOF
  caddy:
    image: caddy:2-alpine
    restart: unless-stopped
    networks: ["${DOCKER_NETWORK}"]
    ports:
      - "80:80"
      - "443:443"
    volumes:
      - ${DATA_ROOT}/caddy/config/Caddyfile:/etc/caddy/Caddyfile
      - ${DATA_ROOT}/caddy/data:/data
      - ${DATA_ROOT}/caddy/config:/config
EOF
fi

if [[ "${ENABLE_OLLAMA}" == "true" ]]; then
cat >> "${COMPOSE_FILE}" << EOF
  ollama:
    image: ollama/ollama
    restart: unless-stopped
    networks: ["${DOCKER_NETWORK}"]
    ports:
      - "11434:11434"
    volumes:
      - ${DATA_ROOT}/ollama:/root/.ollama
    $([ "${GPU_TYPE}" == "nvidia" ] && echo -e "deploy:\\n      resources:\\n        reservations:\\n          devices:\\n            - driver: nvidia\\n              count: all\\n              capabilities: [gpu]")
EOF
fi

if [[ "${ENABLE_QDRANT}" == "true" ]]; then
cat >> "${COMPOSE_FILE}" << EOF
  qdrant:
    image: qdrant/qdrant:latest
    restart: unless-stopped
    networks: ["${DOCKER_NETWORK}"]
    ports:
      - "6333:6333"
    environment:
      QDRANT__SERVICE__API_KEY: ${QDRANT_API_KEY}
    volumes:
      - ${DATA_ROOT}/qdrant:/qdrant/storage
EOF
fi

if [[ "${ENABLE_OPENWEBUI}" == "true" ]]; then
cat >> "${COMPOSE_FILE}" << EOF
  openwebui:
    image: ghcr.io/open-webui/open-webui:main
    restart: unless-stopped
    networks: ["${DOCKER_NETWORK}"]
    ports:
      - "8080:8080"
    environment:
      OLLAMA_BASE_URL: ${OLLAMA_INTERNAL_URL}
    depends_on:
      - ollama
EOF
fi

if [[ "${ENABLE_N8N}" == "true" ]]; then
cat >> "${COMPOSE_FILE}" << EOF
  n8n:
    image: n8nio/n8n:latest
    restart: unless-stopped
    networks: ["${DOCKER_NETWORK}"]
    ports:
      - "5678:5678"
    environment:
      DB_TYPE: postgres
      DB_POSTGRESDB_HOST: postgres
      DB_POSTGRESDB_DATABASE: ${POSTGRES_DB}
      DB_POSTGRESDB_USER: ${POSTGRES_USER}
      DB_POSTGRESDB_PASSWORD: ${POSTGRES_PASSWORD}
      N8N_ENCRYPTION_KEY: ${N8N_ENCRYPTION_KEY}
      N8N_BASIC_AUTH_ACTIVE: "true"
      N8N_BASIC_AUTH_USER: ${N8N_USER}
      N8N_PASSWORD: ${N8N_USER}
    volumes:
      - ${DATA_ROOT}/n8n:/home/node/.n8n
    depends_on:
      - postgres
EOF
fi

if [[ "${ENABLE_FLOWISE}" == "true" ]]; then
cat >> "${COMPOSE_FILE}" << EOF
  flowise:
    image: flowiseai/flowise:latest
    restart: unless-stopped
    networks: ["${DOCKER_NETWORK}"]
    ports:
      - "3000:3000"
    environment:
      DATABASE_TYPE: postgres
      POSTGRES_HOST: postgres
      POSTGRES_DATABASE: ${POSTGRES_DB}
      POSTGRES_USER: ${POSTGRES_USER}
      POSTGRES_PASSWORD: ${POSTGRES_PASSWORD}
      FLOWISE_USERNAME: ${FLOWISE_USERNAME}
      FLOWISE_PASSWORD: ${FLOWISE_PASSWORD}
    volumes:
      - ${DATA_ROOT}/flowise:/root/.flowise
    depends_on:
      - postgres
EOF
fi

if [[ "${ENABLE_ANYTHINGLLM}" == "true" ]]; then
cat >> "${COMPOSE_FILE}" << EOF
  anythingllm:
    image: mintplexlabs/anythingllm:latest
    restart: unless-stopped
    networks: ["${DOCKER_NETWORK}"]
    ports:
      - "3001:3001"
    environment:
      VECTOR_DB: ${VECTOR_DB}
      QDRANT_ENDPOINT: ${QDRANT_INTERNAL_URL}
      QDRANT_API_KEY: ${QDRANT_API_KEY}
      OLLAMA_BASE_URL: ${OLLAMA_INTERNAL_URL}
    volumes:
      - ${DATA_ROOT}/anythingllm:/app/server/storage
    depends_on:
      - ollama
      - qdrant
EOF
fi

if [[ "${ENABLE_LITELLM}" == "true" ]]; then
cat >> "${COMPOSE_FILE}" << EOF
  litellm:
    image: ghcr.io/berriai/litellm:main-latest
    restart: unless-stopped
    networks: ["${DOCKER_NETWORK}"]
    ports:
      - "4000:4000"
    environment:
      LITELLM_MASTER_KEY: ${LITELLM_MASTER_KEY}
EOF
fi

# --- Add Network Definition ---
cat >> "${COMPOSE_FILE}" << EOF

networks:
  ${DOCKER_NETWORK}:
    driver: bridge
EOF

log "docker-compose.yml generated successfully."

# --- Deploy ---
log "Deploying stack with Docker Compose..."
cd "${DATA_ROOT}"
docker compose -p "${COMPOSE_PROJECT_NAME}" up -d
cd -

log "Deployment initiated. Waiting for services to stabilize..."
sleep 60

log "======= Deployment Complete ======="
log "Run \"sudo bash scripts/3-configure-services.sh\" to finalize setup."
