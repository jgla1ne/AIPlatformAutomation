# ── Configuration File Generators ───────────────────────────────────────────
generate_postgres_init() {
    local out="${CONFIG_DIR}/postgres/init-all-databases.sh"
    mkdir -p "$(dirname "$out")"
    # Double-quoted: POSTGRES_PASSWORD expands NOW at generation time.
    # Inner \$\$ escapes are for the psql DO block's dollar-quoting.
    cat > "$out" <<EOF
#!/bin/bash
set -e
psql -v ON_ERROR_STOP=1 --username "\$POSTGRES_USER" --dbname "\$POSTGRES_DB" <<EOSQL
  DO \$\$
  BEGIN
    IF NOT EXISTS (SELECT FROM pg_roles WHERE rolname = '${POSTGRES_USER}') THEN
      CREATE ROLE ${POSTGRES_USER} WITH LOGIN PASSWORD '${POSTGRES_PASSWORD}';
    END IF;
  END \$\$;
  SELECT 'CREATE DATABASE litellm   OWNER ${POSTGRES_USER}'
    WHERE NOT EXISTS (SELECT FROM pg_database WHERE datname='litellm')   \gexec
  SELECT 'CREATE DATABASE openwebui OWNER ${POSTGRES_USER}'
    WHERE NOT EXISTS (SELECT FROM pg_database WHERE datname='openwebui') \gexec
  SELECT 'CREATE DATABASE n8n       OWNER ${POSTGRES_USER}'
    WHERE NOT EXISTS (SELECT FROM pg_database WHERE datname='n8n')       \gexec
  SELECT 'CREATE DATABASE flowise   OWNER ${POSTGRES_USER}'
    WHERE NOT EXISTS (SELECT FROM pg_database WHERE datname='flowise')   \gexec
  GRANT ALL PRIVILEGES ON DATABASE litellm   TO ${POSTGRES_USER};
  GRANT ALL PRIVILEGES ON DATABASE openwebui TO ${POSTGRES_USER};
  GRANT ALL PRIVILEGES ON DATABASE n8n       TO ${POSTGRES_USER};
  GRANT ALL PRIVILEGES ON DATABASE flowise   TO ${POSTGRES_USER};
EOSQL
EOF
    chmod +x "$out"
    # postgres container runs as UID 70 — must be able to read this file
    chown 70:"${TENANT_GID:-1001}" "$out"
    log_success "Postgres init script written to ${out}"
}

generate_litellm_config() {
    log_info "Generating LiteLLM configuration..."
    
    # Enhanced Azure validation to prevent restart loops
    local az_config_valid=true
    if [[ -n "${LITELM_AZURE_API_BASE:-}" ]]; then
        if [[ -z "${LITELM_AZURE_API_KEY:-}" ]]; then
            log_warning "Azure API base configured but missing API key - using local models only"
            az_config_valid=false
        elif [[ "${LITELM_AZURE_API_BASE}" == *"test"* ]]; then
            log_warning "Test Azure endpoints detected - disabling Azure models to prevent failures"
            az_config_valid=false
        fi
    fi
    
    cat > "${CONFIG_DIR}/litellm/config.yaml" << EOF
# LiteLLM Configuration - Generated $(date -u +"%Y-%m-%dT%H:%M:%SZ")
# This file routes LLM requests to appropriate models based on cost/latency

model_list:
  # Local Ollama models (cost-optimized)
EOF
    
    # Add Ollama models dynamically from OLLAMA_MODELS
    [[ -n "${OLLAMA_MODELS:-}" ]] && for m in ${OLLAMA_MODELS//,/ }; do
        cat >> "${CONFIG_DIR}/litellm/config.yaml" <<EOF
  - model_name: ${m}
    litellm_params:
      model: ollama/${m}
      api_base: "http://ollama:11434"
      rpm: 100
EOF
    done
    
    # Only add OpenAI models if API key is provided and not empty
    [[ -n "${OPENAI_API_KEY:-}" && "${OPENAI_API_KEY}" != "" ]] && cat >> "${CONFIG_DIR}/litellm/config.yaml" <<EOF
  - model_name: gpt-4o
    litellm_params:
      model: openai/gpt-4o
      api_key: os.environ/OPENAI_API_KEY
  - model_name: gpt-4o-mini
    litellm_params:
      model: openai/gpt-4o-mini
      api_key: os.environ/OPENAI_API_KEY
EOF
    
    # Only add Azure models if configuration is valid and not empty
    if [[ "$az_config_valid" == "true" ]] && [[ -n "${LITELM_AZURE_API_BASE:-}" && "${LITELM_AZURE_API_BASE}" != "" ]] && [[ -n "${LITELM_AZURE_API_KEY:-}" && "${LITELM_AZURE_API_KEY}" != "" ]]; then
        cat >> "${CONFIG_DIR}/litellm/config.yaml" <<EOF
  - model_name: azure/gpt-4
    litellm_params:
      model: azure/gpt-4
      api_base: "${LITELM_AZURE_API_BASE}"
      api_key: os.environ/LITELM_AZURE_API_KEY
      rpm: 10
  - model_name: azure/gpt-4o
    litellm_params:
      model: azure/gpt-4o
      api_base: "${LITELM_AZURE_API_BASE}"
      api_key: os.environ/LITELM_AZURE_API_KEY
      rpm: 20
EOF
    fi
    
    # Only add Groq models if API key is provided and not empty
    [[ -n "${GROQ_API_KEY:-}" && "${GROQ_API_KEY}" != "" ]] && cat >> "${CONFIG_DIR}/litellm/config.yaml" <<EOF
  - model_name: llama3-groq
    litellm_params:
      model: groq/llama3-70b-8192
      api_key: os.environ/GROQ_API_KEY
EOF
    
    # Only add Anthropic models if API key is provided and not empty
    [[ -n "${ANTHROPIC_API_KEY:-}" && "${ANTHROPIC_API_KEY}" != "" ]] && cat >> "${CONFIG_DIR}/litellm/config.yaml" <<EOF
  - model_name: claude-3-5-sonnet
    litellm_params:
      model: anthropic/claude-3-5-sonnet-20241022
      api_key: os.environ/ANTHROPIC_API_KEY
EOF
    
    # Only add Gemini models if API key is provided and not empty
    [[ -n "${GOOGLE_API_KEY:-}" && "${GOOGLE_API_KEY}" != "" ]] && cat >> "${CONFIG_DIR}/litellm/config.yaml" <<EOF
  - model_name: gemini-pro
    litellm_params:
      model: google/gemini-pro
      api_key: os.environ/GOOGLE_API_KEY
EOF
    
    # Only add OpenRouter models if API key is provided and not empty
    [[ -n "${OPENROUTER_API_KEY:-}" && "${OPENROUTER_API_KEY}" != "" ]] && cat >> "${CONFIG_DIR}/litellm/config.yaml" <<EOF
  - model_name: openrouter-mixtral
    litellm_params:
      model: openrouter/mistralai/mixtral-8x7b
      api_key: os.environ/OPENROUTER_API_KEY
EOF
    
    cat >> "${CONFIG_DIR}/litellm/config.yaml" <<EOF
litellm_settings:
  drop_params: true
  set_verbose: false
  cache: true
  cache_params:
    type: redis
    host: redis
    port: 6379
    password: os.environ/REDIS_PASSWORD
router_settings:
  routing_strategy: ${LITELLM_ROUTING_STRATEGY:-least-busy}
EOF
    
    # Only add fallbacks for models that are actually configured
    if [[ -n "${OPENAI_API_KEY:-}" && "${OPENAI_API_KEY}" != "" ]] || [[ -n "${GROQ_API_KEY:-}" && "${GROQ_API_KEY}" != "" ]] || [[ -n "${GOOGLE_API_KEY:-}" && "${GOOGLE_API_KEY}" != "" ]] || [[ -n "${OPENROUTER_API_KEY:-}" && "${OPENROUTER_API_KEY}" != "" ]]; then
        # Build fallback list from only configured models
        local external_models=()
        [[ -n "${OPENAI_API_KEY:-}" && "${OPENAI_API_KEY}" != "" ]] && external_models+=("gpt-4o")
        [[ -n "${GROQ_API_KEY:-}" && "${GROQ_API_KEY}" != "" ]] && external_models+=("llama3-groq")
        [[ -n "${GOOGLE_API_KEY:-}" && "${GOOGLE_API_KEY}" != "" ]] && external_models+=("gemini-pro")
        [[ -n "${OPENROUTER_API_KEY:-}" && "${OPENROUTER_API_KEY}" != "" ]] && external_models+=("openrouter-mixtral")

        if [[ ${#external_models[@]} -gt 1 ]]; then
            cat >> "${CONFIG_DIR}/litellm/config.yaml" <<EOF
  fallbacks:
EOF
            for model in "${external_models[@]}"; do
                # Build fallback list = all other external models
                local others=()
                for other in "${external_models[@]}"; do
                    [[ "$other" != "$model" ]] && others+=("\"$other\"")
                done
                local others_str
                others_str=$(IFS=,; echo "${others[*]}")
                cat >> "${CONFIG_DIR}/litellm/config.yaml" <<EOF
    - ${model}: [${others_str}]
EOF
            done
        fi
    fi
    
    cat >> "${CONFIG_DIR}/litellm/config.yaml" <<EOF
general_settings:
  master_key: os.environ/LITELLM_MASTER_KEY
  database_url: os.environ/LITELLM_DATABASE_URL
EOF
    log_success "LiteLLM config written to ${CONFIG_DIR}/litellm/config.yaml"
}

generate_caddyfile() {
    local out="${CONFIG_DIR}/caddy/Caddyfile"
    mkdir -p "$(dirname "$out")"

    # Choose TLS directive once based on USE_LETSENCRYPT
    local tls_line
    if [[ "${USE_LETSENCRYPT:-false}" == "true" ]]; then
        tls_line="tls ${ADMIN_EMAIL}"
    else
        tls_line="tls internal"
    fi

    # Write global block — heredoc closed BEFORE conditional appends
    cat > "$out" <<EOF
{
    admin 0.0.0.0:2019
    email ${ADMIN_EMAIL:-admin@${DOMAIN:-localhost}}
}
EOF

    # Append one block per enabled service — each is a separate cat call
    [[ "${ENABLE_MONITORING:-false}" == "true" ]] && cat >> "$out" <<EOF
grafana.${DOMAIN} {
    ${tls_line}
    reverse_proxy grafana:3000
}
prometheus.${DOMAIN} {
    ${tls_line}
    reverse_proxy prometheus:9090
}
EOF

    [[ "${ENABLE_LITELLM:-false}" == "true" ]] && cat >> "$out" <<EOF
litellm.${DOMAIN} {
    ${tls_line}
    reverse_proxy litellm:4000
}
EOF

    [[ "${ENABLE_OPENWEBUI:-false}" == "true" ]] && cat >> "$out" <<EOF
chat.${DOMAIN} {
    ${tls_line}
    reverse_proxy open-webui:8080
}
EOF

    [[ "${ENABLE_N8N:-false}" == "true" ]] && cat >> "$out" <<EOF
n8n.${DOMAIN} {
    ${tls_line}
    reverse_proxy n8n:5678
}
EOF

    [[ "${ENABLE_FLOWISE:-false}" == "true" ]] && cat >> "$out" <<EOF
flowise.${DOMAIN} {
    ${tls_line}
    reverse_proxy flowise:3000
}
EOF

    [[ "${ENABLE_ANYTHINGLLM:-false}" == "true" ]] && cat >> "$out" <<EOF
anythingllm.${DOMAIN} {
    ${tls_line}
    reverse_proxy anythingllm:3001
}
EOF

    [[ "${ENABLE_CODESERVER:-false}" == "true" ]] && cat >> "$out" <<EOF
codeserver.${DOMAIN} {
    ${tls_line}
    reverse_proxy codeserver:8443
}
EOF

    # OpenCode - Primary IDE access
    [[ "${ENABLE_CODESERVER:-false}" == "true" ]] && cat >> "$out" <<EOF
opencode.${DOMAIN} {
    ${tls_line}
    reverse_proxy codeserver:8443
}
EOF

    # OpenClaw - routes to Docker service for secure access
    [[ "${ENABLE_OPENCLAW:-false}" == "true" ]] && cat >> "$out" <<EOF
openclaw.${DOMAIN} {
    ${tls_line}
    reverse_proxy openclaw:8443
}
EOF

    log_success "Caddyfile written to ${out}"
}

