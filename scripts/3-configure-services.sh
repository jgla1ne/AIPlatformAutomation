# ── Docker Compose Generator (Zero Hardcoded Values) ───────────────────────
generate_compose() {
    log_info "Generating docker-compose.yml at ${COMPOSE_FILE}..."
    
    cat > "$COMPOSE_FILE" <<'EOF'
networks:
  default:
    name: ai-${TENANT}-net
    driver: bridge

volumes:
  postgres_data:
  prometheus_data:
  grafana_data:
  litellm_data:

services:

  postgres:
    image: postgres:15-alpine
    restart: unless-stopped
    user: "${POSTGRES_UID:-70}:${TENANT_GID:-1001}"
    environment:
      POSTGRES_DB: ${POSTGRES_DB}
      POSTGRES_USER: ${POSTGRES_USER}
      POSTGRES_PASSWORD: ${POSTGRES_PASSWORD}
    volumes:
      - postgres_data:/var/lib/postgresql/data
      - ${CONFIG_DIR}/postgres/init-all-databases.sh:/docker-entrypoint-initdb.d/init-all-databases.sh:ro
    healthcheck:
      test: ["CMD-SHELL","pg_isready -U ${POSTGRES_USER} -d ${POSTGRES_DB}"]
      interval: 10s
      timeout: 5s
      retries: 5
      start_period: 30s

  redis:
    image: redis:7-alpine
    restart: unless-stopped
    user: "${REDIS_UID:-999}:${TENANT_GID:-1001}"
    command: redis-server --requirepass "${REDIS_PASSWORD}"
    volumes:
      - ${DATA_DIR}/redis:/data
    healthcheck:
      test: ["CMD","redis-cli","-a","${REDIS_PASSWORD}","ping"]
      interval: 5s
      timeout: 5s
      retries: 5
EOF

    # Add enabled services dynamically - NO hardcoded values
    [[ "${ENABLE_LITELLM:-false}" == "true" ]] && cat >> "$COMPOSE_FILE" <<'EOF'
  litellm:
    image: ghcr.io/berriai/litellm:main
    restart: unless-stopped
    user: "root"
    depends_on:
